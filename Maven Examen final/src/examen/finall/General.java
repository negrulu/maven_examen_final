package examen.finall;

public class General {
	String nombre;
	String descripcion;
	int anioVigencia;
	int codigoPoliza;
	int valorPoliza;
	
	int codigoDescuento;
	int numeroSeguro;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getAnioVigencia() {
		return anioVigencia;
	}
	public void setAnioVigencia(int anioVigencia) {
		this.anioVigencia = anioVigencia;
	}
	public int getCodigoPoliza() {
		return codigoPoliza;
	}
	public void setCodigoPoliza(int codigoPoliza) {
		this.codigoPoliza = codigoPoliza;
	}
	public double getValorPoliza() {
		return valorPoliza;
	}
	public void setValorPoliza(int valorPoliza) {
		this.valorPoliza = valorPoliza;
	}
	public int getCodigoDescuento() {
		return codigoDescuento;
	}
	public void setCodigoDescuento(int codigoDescuento) {
		this.codigoDescuento = codigoDescuento;
	}
	public int getNumeroSeguro() {
		return numeroSeguro;
	}
	public void setNumeroSeguro(int numeroSeguro) {
		this.numeroSeguro = numeroSeguro;
	}
	
	

}
